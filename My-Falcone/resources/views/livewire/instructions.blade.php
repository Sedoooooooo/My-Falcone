    <h1 class="text-3xl font-bold mb-6">How to Play: Finding Falcone</h1>

    <p class="text-lg mb-4">
        Welcome to the "Finding Falcone" game! In this game, you’ll need to choose planets and vehicles to search for the elusive planet Falcone.
    </p>

    <p class="text-lg font-semibold mb-4">Step 1: Select Planets (Destination 1-4)</p>
    <p class="mb-4">
        <strong>What to do:</strong> For each of the four destinations (Destination 1 to Destination 4), you’ll need to select a planet to search. Simply click on the dropdown menu under each destination and choose a planet from the list.
    </p>
    <p class="mb-4">
        <strong>Why:</strong> Each destination needs a planet for the search. The planets you select will affect the total time it takes to complete the mission.
    </p>

    <p class="text-lg font-semibold mb-4">Step 2: Select Vehicles for Each Destination</p>
    <p class="mb-4">
        <strong>What to do:</strong> Once you’ve selected a planet for a destination, choose a vehicle for that destination. Each destination needs its own vehicle, and each vehicle can only be selected once per destination.
    </p>
    <p class="mb-4">
        <strong>Why:</strong> Different vehicles have different speeds and distances they can travel. This will impact the total time taken to find Falcone, so choose wisely!
    </p>

    <p class="text-lg font-semibold mb-4">Step 3: Check the Time Taken</p>
    <p class="mb-4">
        <strong>What to do:</strong> As you make your selections for planets and vehicles, the <strong>Time Taken</strong> field will automatically update. This shows the total time it will take for your selected vehicles to reach the selected planets.
    </p>
    <p class="mb-4">
        <strong>Why:</strong> Time is critical in this mission! The faster your selections, the better your chances of finding Falcone. Keep an eye on the time to ensure a quick and efficient search!
    </p>

    <p class="text-lg font-semibold mb-4">Step 4: Find Falcone!</p>
    <p class="mb-4">
        <strong>What to do:</strong> Once you've selected planets and vehicles for all four destinations, the <strong>Find Falcone!</strong> button will become enabled. Click this button to finalize your selection and submit the mission.
    </p>
    <p class="mb-4">
        <strong>Why:</strong> Clicking <strong>Find Falcone!</strong> will complete your mission and show you the results: whether you found Falcone or not!
    </p>

    <p class="text-lg font-semibold mb-4">Step 5: Reset</p>
    <p class="mb-4">
        <strong>What to do:</strong> If you want to start over and make new selections, simply click on the <strong>Reset</strong> button. This will clear all your selections and allow you to try again.
    </p>
    <p class="mb-4">
        <strong>Why:</strong> This option is useful if you want to adjust your strategy or make different selections for planets and vehicles.
    </p>

